#Arithmetic Operators (+, -, *, /, %, **, //)

val_A = 10
val_B = 2

print(val_A + val_B)
print(val_A - val_B)

print(val_A / val_B)
print(val_A % val_B)

print(val_A ** val_B)
print(val_A // val_B)

#Identity Operators
q = ["luis"]
r = ["luis"]

print(q is r)


#Membership Operator
fruits = ["apple", "banana"]

if "apple" in fruits:

  print("Yes, apple is a fruit!")

#Bitwise
if 5 == 10 or 4 == 4:

  print("At least one of the statements is true")  