#Text Type Strings: str

a = "Hello World this is my first string"

print(a)

#Numeric Types: int, float, complex

#int
b = 40
print(b)

#float
c = 40.5
print(c)

#complex
d = 7
print(d)
print(type(d))

#Sequence Types: list, tuple, range

#list
e = ["windows", "macOS", "Linux"]
print(e)
print(type(e))

#tuple
f = ("windows", "macOS", "Linux")
print(f)
print(type(f))

#range
g = range(10)
print(g)
print(type(g))

#Set Types: set, frozenset  

#set
j = {"windows", "macOS", "linux"}
print(j)
print(type(j))

#frozenset
k=({"windows", "macOS", "linux"})
print(k)
print(type(k))

#Boolean: bool

l = True
print(l)
print(type(l))

m = False
print(m)
print(type(m))

#Binary Types: bytes, bytearray, memoryview

#bytes
n = b"Hello"
print(n)
print(type(n))

#bytearray
o = bytearray(2)
print(o)
print(type(o))

#memoryview
p = memoryview(bytes(2))
print(p)
